    <p>"Champagne problems" bercerita tentang kisah seorang wanita yang mengejutkan calon tunangannya dan orang yang mereka cintai dengan menolak lamaran pernikahan dan membiarkannya menggantung (tepat sebelum Natal). Swift menyatakan bahwa lagu itu adalah "lagu di mana kekasih lama saat kuliah memiliki rencana yang sangat berbeda untuk malam yang sama, satu untuk mengakhirinya dan satu yang membawa cincin." Tokoh protagonis tampaknya memiliki riwayat penyakit mental, yang kemudian dicap kota dengan gosip mereka tentang lamaran yang gagal. Sementara "Champagne problems" merupakan penggambaran bahwa sebuah masalah bukanlah masalah yang besar jika dibandingkan dengan masalah kemiskinan, bencana nasional, dan perang, tetapi bagi individu itu adalah masalah yang harus ditangani.</p>

