    <p>'Magic Shop' adalah lagu dari BTS. Album ini dirilis pada 18 Mei 2018.Lagu ini muncul sebagai lagu ketujuh untuk album studio kedua mereka Love Yourself: Tear dan sebagai lagu pertama CD B dalam album kompilasi kedua Love Yourself: Answer. Lagu ini ditulis oleh Jungkook, Hiss Noise, RM, DJ Swivel, Candace Nicole Sosa, ADORA, J-Hope, Suga.Magic Shop merupakan lagu BTS untuk Army (nama fans mereka). Army adalah alasan BTS bertahan sampai sekarang. Army telah memberi yang terbaik untuk BTS, dan BTS berharap Army juga melakukan yang terbaik untuk diri mereka sendiri. Dalam lagu itu ada lirik yang maknanya, saat kamu membenci dirimu sendiri. Saat kamu ingin menghilang untuk selamanya. Buatlah sebuah pintu di hatimu. Buka pintunya dan kamu akan menemukan Magic Shop. Semua jawaban yang kamu inginkan ada di tempat yang kamu temukan itu. Di dalam hatimu.</p>
